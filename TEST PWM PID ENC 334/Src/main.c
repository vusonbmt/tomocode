float kp= 30;
float ki= 71;
float kd= 0.65;
#define dt_ 0.005
#define inv_dt 200

float P=0,I=0,D=0,U=0,e,e0;




void PID(signed int real, signed int set)
{
    e=(float)set-(float)real;
    P=kp*e;
    I+=ki*e*dt_;
    D= kd*(e-e0)*inv_dt;
    U=P+I+D;
    e0=e;
    duty=(uint16_t)U/100;
	  if(duty>499) 
			{
				duty=499;
			}

}



